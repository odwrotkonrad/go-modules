// TestRenderRepoMultiDestAtInclude: one dest inlines @-includes, the other keeps them.
func TestRenderRepoMultiDestAtInclude(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"templates/agents.ontoRepo.tpl": "@inc.md\n",
		"inc.md":                        "INLINED\n",
	})
	h := New(root, "/home/x", "cli/macos", config.Config{})
	item := spec.FileItem{
		Rel: "templates/agents.ontoRepo.tpl",
		Dests: []spec.DestSpec{
			{Path: "CLAUDE.md"},
			{Path: "AGENTS.md", Options: render.Options{RenderReferencedFiles: true}},
		},
	}
	if err := h.RenderTemplates([]spec.FileItem{item}, false); err != nil {
		t.Fatal(err)
	}
	claude := readDest(t, root, "CLAUDE.md")
	agents := readDest(t, root, "AGENTS.md")
	if !strings.Contains(claude, "@inc.md") {
		t.Errorf("CLAUDE.md should keep @-include, got %q", claude)
	}
	if !strings.Contains(agents, "INLINED") || strings.Contains(agents, "@inc.md") {
		t.Errorf("AGENTS.md should inline @-include, got %q", agents)
	}
}

// TestRenderContinuesOnError: a failing template logs render(fail), the next
// item still renders, and the op returns the failure.
func TestRenderContinuesOnError(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"templates/ok.ontoRepo.tpl": "fine\n",
	})
	h := New(root, "/home/x", "cli/macos", config.Config{})
	items := []spec.FileItem{
		{Rel: "templates/missing.ontoRepo.tpl", Dests: []spec.DestSpec{{Path: "bad.md"}}},
		{Rel: "templates/ok.ontoRepo.tpl", Dests: []spec.DestSpec{{Path: "ok.md"}}},
	}
	out, err := testutil.CaptureStdout(t, func() error { return h.RenderTemplates(items, false) })
	if err == nil {
		t.Fatal("RenderTemplates must return the collected failure")
	}
	testutil.WantLines(t, out,
		"render(fail): templates/missing.ontoRepo.tpl: ",
		"render(create): "+filepath.Join(root, "ok.md"))
	if got := readDest(t, root, "ok.md"); !strings.Contains(got, "fine") {
		t.Errorf("second template not rendered after first failed: %q", got)
	}
	if _, serr := os.Stat(filepath.Join(root, "bad.md")); !os.IsNotExist(serr) {
		t.Error("failed template unexpectedly produced bad.md")
	}
}

// TestRenderSkipSecrets: op:// source skipped when skipSecrets is set.
func TestRenderSkipSecrets(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"templates/s.ontoRepo.tpl": "TOKEN={{ op \"op://x/y/z\" }}\n",
	})
	h := New(root, "/home/x", "cli/macos", config.Config{})
	item := spec.FileItem{Rel: "templates/s.ontoRepo.tpl", Dests: []spec.DestSpec{{Path: "out"}}}
	if err := h.RenderTemplates([]spec.FileItem{item}, true); err != nil {
		t.Fatal(err)
	}
	if _, err := os.Stat(filepath.Join(root, "out")); !os.IsNotExist(err) {
		t.Errorf("secret-ref dest should be skipped, but out exists")
	}
}

