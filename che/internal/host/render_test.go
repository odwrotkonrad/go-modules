// TestRenderRemoteSourceWithCtx: @-prefixed remote source fetched through the
// RemoteFetcher, ctx as root context, skipAutoGeneratedHeader -> no header.
func TestRenderRemoteSourceWithCtx(t *testing.T) {
	root := testutil.Tree(t, nil)
	h := New(root, "/home/x", "cli/macos", config.Config{}).WithFetcher(testutil.RemoteMockFetcher{
		"gitlab.com/acme/tools//snippets/agent.md": "name: RO-{{ .repo }}\n",
	})
	item := spec.FileItem{
		Rel:   "@gitlab.com/acme/tools//snippets/agent.md",
		Ctx:   map[string]string{"repo": "Notes"},
		Dests: []spec.DestSpec{{Path: ".claude/agents/ro.md", Options: render.Options{SkipAutoGeneratedHeader: true}}},
	}
	require.NoError(t, h.RenderTemplates([]spec.FileItem{item}, false))
	assert.Equal(t, "name: RO-Notes\n", readDest(t, root, ".claude/agents/ro.md"))
}

