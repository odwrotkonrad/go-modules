// writeRepo writes rel->content under a temp repo root, returns the root.
func writeRepo(t *testing.T, files map[string]string) string {
	t.Helper()
	root := t.TempDir()
	for rel, body := range files {
		p := filepath.Join(root, rel)
		if err := os.MkdirAll(filepath.Dir(p), 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(p, []byte(body), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	return root
}

func readDest(t *testing.T, root, rel string) string {
	t.Helper()
	b, err := os.ReadFile(filepath.Join(root, rel))
	if err != nil {
		t.Fatalf("read %s: %v", rel, err)
	}
	return string(b)
}

// TestRenderRepoReplace: relative dest -> repo file, default writeType
// (overwrite) writes Header + rendered body.
func TestRenderRepoReplace(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"templates/x.ontoRepo.tpl": "hello {{ \"world\" }}\n",
	})
	h := New(root, "/home/x", "cli/macos", config.Config{})
	item := spec.FileItem{Rel: "templates/x.ontoRepo.tpl", Dests: []spec.DestSpec{{Path: "out.md"}}}
	if err := h.RenderTemplates([]spec.FileItem{item}, false); err != nil {
		t.Fatal(err)
	}
	got := readDest(t, root, "out.md")
	want := "<!-- autogenerated using templates/x.ontoRepo.tpl -->\nhello world\n"
	if got != want {
		t.Errorf("replace output = %q, want %q", got, want)
	}
}

// TestRenderRepoMergeUpsert: existing wins, defaults fill, sorted, no header.
func TestRenderRepoMergeUpsert(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"templates/env.ontoRepo.tpl": "B=default\nA=default\n",
		".env":                       "B=kept\nC=extra\n",
	})
	h := New(root, "/home/x", "cli/macos", config.Config{})
	item := spec.FileItem{
		Rel:   "templates/env.ontoRepo.tpl",
		Dests: []spec.DestSpec{{Path: ".env", Options: render.Options{WriteType: "mergeUpsert"}}},
	}
	if err := h.RenderTemplates([]spec.FileItem{item}, false); err != nil {
		t.Fatal(err)
	}
	got := readDest(t, root, ".env")
	want := "A=default\nB=kept\nC=extra\n"
	if got != want {
		t.Errorf("merge-upsert output = %q, want %q", got, want)
	}
	// stable: re-run yields the same content
	if err := h.RenderTemplates([]spec.FileItem{item}, false); err != nil {
		t.Fatal(err)
	}
	if again := readDest(t, root, ".env"); again != want {
		t.Errorf("merge-upsert not stable: %q", again)
	}
}

// TestRenderHostMergeUpsert: an absolute (host) dest also reads Existing, so
// mergeUpsert works on host files.
func TestRenderHostMergeUpsert(t *testing.T) {
	root := writeRepo(t, map[string]string{
		"root/HOME/env.ontoHost.tpl": "B=default\nA=default\n",
	})
	home := filepath.Join(root, "home")
	if err := os.MkdirAll(home, 0o755); err != nil {
		t.Fatal(err)
	}
	dest := filepath.Join(home, ".env")
	if err := os.WriteFile(dest, []byte("B=kept\nC=extra\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	h := New(root, home, "cli/macos", config.Config{})
	item := spec.FileItem{
		Rel:   "root/HOME/env.ontoHost.tpl",
		Dests: []spec.DestSpec{{Path: "~/.env", Options: render.Options{WriteType: "mergeUpsert"}}},
	}
	if err := h.RenderTemplates([]spec.FileItem{item}, false); err != nil {
		t.Fatal(err)
	}
	got, err := os.ReadFile(dest)
	if err != nil {
		t.Fatal(err)
	}
	want := "A=default\nB=kept\nC=extra\n"
	if string(got) != want {
		t.Errorf("host merge-upsert output = %q, want %q", got, want)
	}
}

