// observabilityDoc renders the telemetry reference from the telemetry.Metrics
// registry (metric table) and the curated labelValues (per-label vocabularies),
// plus static prose on the log bridge and config.
func observabilityDoc() string {
	var b strings.Builder
	b.WriteString("<!-- autogenerated using internal/docgen -->\n")
	b.WriteString("# che observability\n\n")
	b.WriteString("che emits OTLP telemetry (metrics + mirrored logs) to a local collector, wired in `internal/telemetry`. Disabled by default; a nil provider is a no-op.\n\n")

	b.WriteString("## Metrics\n\n")
	b.WriteString("Every metric is an Int64 monotonic counter on meter `che`.\n\n")
	b.WriteString("| Metric | Type | Labels | Description |\n| --- | --- | --- | --- |\n")
	for _, m := range telemetry.Metrics {
		var labels []string
		for _, l := range m.Labels {
			labels = append(labels, tick(l))
		}
		fmt.Fprintf(&b, "| `%s` | counter | %s | %s |\n", m.Name, strings.Join(labels, ", "), m.Help)
	}

	b.WriteString("\n## Label values\n\n")
	b.WriteString("Observed values per label, curated from the emission sites in `internal/che` (not an enforced enum).\n")
	for _, lv := range labelValues {
		fmt.Fprintf(&b, "\n### `%s`\n\n", lv.Label)
		b.WriteString("| Value | Meaning |\n| --- | --- |\n")
		for _, v := range lv.Values {
			fmt.Fprintf(&b, "| `%s` | %s |\n", v.Value, v.Meaning)
		}
	}

	b.WriteString("\n## Traces\n\n")
	b.WriteString("When `otel.traces` is on, che emits spans on tracer `che`. The tree nests `che run` > `prepare-specs` / `<command>` > `profile` > `<operation>` > external-call spans (`fetch-remote`, `run-script`). Counters record under the active span ctx, so metric exemplars link back to the trace.\n\n")
	b.WriteString("| Span | Attributes | Description |\n| --- | --- | --- |\n")
	for _, s := range telemetry.Spans {
		var attrs []string
		for _, a := range s.Attrs {
			attrs = append(attrs, tick(a))
		}
		fmt.Fprintf(&b, "| `%s` | %s | %s |\n", s.Name, strings.Join(attrs, ", "), s.Help)
	}

	b.WriteString("\n## Logs\n\n")
	b.WriteString("When `otel.logs` is on, each che log line is mirrored as an OTLP log record via the log bridge (`Telemetry.LogRecord`): the log title becomes the record event name, the message becomes the body, and the level maps to severity (`debug` -> Debug, everything else -> Info).\n")

	b.WriteString("\n## Config\n\n")
	b.WriteString("The `telemetry.Config` knobs, driven by the `otel:` spec block and `CHE_OTEL_*` env (env wins). See the `otel` group in [che.yml schema](../assets/data/che.schema.json).\n\n")
	b.WriteString("| Knob | Env | Default | Description |\n| --- | --- | --- | --- |\n")
	b.WriteString("| `enabled` | `CHE_OTEL_ENABLED` | `false` | emit OTLP telemetry to the collector |\n")
	b.WriteString("| `endpoint` | `CHE_OTEL_ENDPOINT` | `localhost:4317` (grpc) / `localhost:4318` (http) | OTLP collector endpoint (host:port) |\n")
	b.WriteString("| `protocol` | `CHE_OTEL_PROTOCOL` | `grpc` | OTLP transport: `grpc` \\| `http` |\n")
	b.WriteString("| `metrics` | `CHE_OTEL_METRICS` | `true` | export the metrics above |\n")
	b.WriteString("| `logs` | `CHE_OTEL_LOGS` | `true` | export che log lines as OTLP logs |\n")
	b.WriteString("| `traces` | `CHE_OTEL_TRACES` | `true` | export the spans above |\n")
	return b.String()
}

