func TestRenderMarkdown(t *testing.T) {
	root := t.TempDir()
	writeFile(t, filepath.Join(root, "p.md"),
		"---\npaths:\n  - x\n---\n\n## Heading\n\n<!-- drop me -->\ntext\n")
	got, err := RenderMarkdown(root, "p.md", "remove-frontmatter", "strip-comments", "normalize-headings")
	if err != nil {
		t.Fatal(err)
	}
	want := "### Heading\n\ntext"
	if got != want {
		t.Errorf("RenderMarkdown = %q, want %q", got, want)
	}
	// frontmatter kept when the opt is omitted
	kept, _ := RenderMarkdown(root, "p.md", "strip-comments")
	if !strings.HasPrefix(kept, "---") {
		t.Errorf("frontmatter should be kept without remove-frontmatter: %q", kept)
	}
	// unknown opt errors
	if _, err := RenderMarkdown(root, "p.md", "bogus"); err == nil {
		t.Error("unknown opt should error")
	}
}

